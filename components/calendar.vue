<template>
  <div>
    <div class="columns">
      <div class="column is-one-third"></div>
      <div class="column is-one-third has-text-centered">
        <span>{{ monthName }}</span>
      </div>
      <div class="column is-one-third">
        <div class="is-pulled-right">
          <b-field>
            <b-radio-button
              v-model="componentWeatherUnits"
              :native-value="FAHRENHEIT_STRING"
            >
              <span>Fahrenheit</span>
            </b-radio-button>
            <b-radio-button
              v-model="componentWeatherUnits"
              :native-value="CELSIUS_STRING"
            >
              <span>Celsius</span>
            </b-radio-button>
          </b-field>
        </div>
      </div>
    </div>
    <div class="columns">
      <div
        v-for="weekDayNumber in weekDayNumbers"
        :key="weekDayNumber"
        class="column is-paddingless"
      >
        <calendar-column :week-day-number="weekDayNumber" />
      </div>
    </div>
    <reminder-modal />
  </div>
</template>

<script>
import { mapState, mapMutations } from 'vuex'
import CalendarColumn from '~/components/calendar-column'
import ReminderModal from '~/components/reminder-modal'

import { CELSIUS_STRING, FAHRENHEIT_STRING } from '~/utils/constants'

export default {
  name: 'Calendar',
  components: { CalendarColumn, ReminderModal },
  data() {
    return {
      CELSIUS_STRING,
      FAHRENHEIT_STRING
    }
  },
  computed: {
    ...mapState('calendar', ['weekDaysMap', 'monthName']),
    ...mapState('weather', { weatherUnits: 'units' }),
    componentWeatherUnits: {
      get() {
        return this.weatherUnits
      },
      set(newUnits) {
        this.setWeatherUnits(newUnits)
      }
    },
    weekDayNumbers() {
      return this.weekDaysMap.map(weekDayObj => weekDayObj.number)
    }
  },
  methods: {
    ...mapMutations('weather', { setWeatherUnits: 'setUnits' })
  }
}
</script>
